#include "DrawableObjectCube.h"


DrawableObjectCube::DrawableObjectCube()
{
	SetWorldMatrix(new XMFLOAT4X4());
	NUM_VERTICES = 36;

}

DrawableObjectCube::~DrawableObjectCube()
{
	delete m_World;
	m_World = nullptr;
	Release();
}

